const BASE_URL = 'YOUR BASE URL';

const ENDPOINT = {
  predict: `YOUR PATH TO ENDPOINT`,
};

class PredictAPI {
  static async predict(data) {
    const response = await fetch(ENDPOINT.predict, {
      method: 'POST',
      body: data,
      redirect: 'follow',
    });

    const json = await response.json();
    return json;
  }
}
